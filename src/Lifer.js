function LifeDoc(lifediv) {
  this.lifeGrid = new LifeGrid(lifediv);
  this.topdiv   = lifediv;
  this.longestDimension    = 300;      // width in pixels

  this.cellWidth  = 10;
  this.cellHeight = 10;
  document.lifeIsRunning = false;
}

function onClickLifeCell(id) {
  td = document.getElementById(id);
  var x = td.getAttribute("data-x");
  var y = td.getAttribute("data-y");

  oldval = document.lifeDoc.lifeGrid.valueAt(x,y);
  newval = 1-oldval;
  document.lifeDoc.lifeGrid.setValueAt(x,y, newval);
  document.lifeDoc.setGridColors();
  // alert ('click: (' + x + ', ' + y + ')');
//  alert ("this is " + this);
}

function startOrStopLife() {
  if (document.lifeIsRunning) {
    document.lifeIsRunning = false;

  } else {
    document.lifeIsRunning = true;
    runLife();
  }
}

function runLife() {
  document.lifeDoc.runOneCycle();
  if (document.lifeIsRunning)
    var timer = setTimeout("runLife()", 500);
}

